package model.entity;

public class Aluno {
	
	private String nomeAluno;
	private int prontuarioAluno;
	private float iraAluno;
	private String cpfAluno;
	private String emailAluno;
	
	public Aluno() {

	}

	public String getNomeAluno() {
		return nomeAluno;
	}

	public void setNomeAluno(String nomeAluno) {
		this.nomeAluno = nomeAluno;
	}

	public int getProntuarioAluno() {
		return prontuarioAluno;
	}

	public void setProntuarioAluno(int prontuarioAluno) {
		this.prontuarioAluno = prontuarioAluno;
	}

	public float getIraAluno() {
		return iraAluno;
	}

	public void setIraAluno(float iraAluno) {
		this.iraAluno = iraAluno;
	}

	public String getCpfAluno() {
		return cpfAluno;
	}

	public void setCpfAluno(String cpfAluno) {
		this.cpfAluno = cpfAluno;
	}

	public String getEmailAluno() {
		return emailAluno;
	}

	public void setEmailAluno(String emailAluno) {
		this.emailAluno = emailAluno;
	}
	
	
}
